    public void setCurrentFrame(long currentFrame){
        frame = currentFrame;
        lblCurrent.setText(currentFrame+"");
        lblTotal.setText(totalFrames+"");
        lblRemain.setText((totalFrames-currentFrame)+"");
        float percent = currentFrame * 100f / totalFrames;
        lblPercent.setText(Math.round(percent)+"%");
        pbDisplay.setValue(Math.round(percent));
    }

